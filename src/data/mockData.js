// 构建小写查词索引（供 enrichSegmented / 缓存使用）
export function buildDictIndex(rows = mockDictionary) {
  const map = {}
  for (const r of rows) {
    const senses = r.senses || []
    const meanings = senses.map((s) => s.meaning).filter(Boolean)
    map[r.word.toLowerCase()] = { word: r.word, meanings }
  }
  return map
}
